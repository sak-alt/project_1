package GeneralCoreJavaProgram.DataTypes;

public class OperationsOnDataTypes {
    public static void main(String[] args) {
        String value = "10";
        int values = Integer.parseInt(value);
        System.out.println(values);

        //Default value of Integer is null.
        Integer values1 = null;
        System.out.println(values1);

        //Default value of int is Zero.
        int values2 = 0;
        System.out.println(values2 + 10);
    }
}
