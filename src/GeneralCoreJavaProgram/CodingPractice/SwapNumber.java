package GeneralCoreJavaProgram.CodingPractice;

public class SwapNumber {
    public static void main(String[] args) {
        int a = 20;
        int b = 30;
        int temp = a;
        a = b;
        b = temp;

        System.out.println("The Value of a is ::" + a);
        System.out.println("The Value of b is ::" + b);
    }
}
