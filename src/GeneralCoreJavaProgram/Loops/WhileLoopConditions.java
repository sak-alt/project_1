package GeneralCoreJavaProgram.Loops;

public class WhileLoopConditions {
    public static void main(String[] args) {
        int i = 11, range = 10;

        while (i <= range) {
            System.out.println(i);
            i++;
        }
        System.out.println("The Loop is broken because of exceeding i value.");
    }
}
